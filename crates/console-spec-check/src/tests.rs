const FIXTURE: &str = include_str!("../tests/data/parity_fixture.md");
const GOLDEN: &str = include_str!("../tests/data/parity_golden.json");

#[test]
fn gap_id_parity_with_vendored_python_primitive() -> Result<(), Box<dyn std::error::Error>> {
    let golden: serde_json::Value = serde_json::from_str(GOLDEN)?;
    let golden = golden.as_array().ok_or("golden is not a JSON array")?;
    let rules = extract_rules("parity_fixture.md", FIXTURE);
    assert_eq!(rules.len(), golden.len(), "rule count vs golden");
    for (rule, want) in rules.iter().zip(golden.iter()) {
        let want = want.as_object().ok_or("golden entry is not an object")?;
        let field = |key: &str| want.get(key).and_then(serde_json::Value::as_str);
        assert_eq!(Some(rule.spec_file.as_str()), field("spec_file"));
        assert_eq!(Some(rule.heading_path.as_str()), field("heading_path"));
        assert_eq!(Some(rule.line_text.as_str()), field("line_text"));
        assert_eq!(
            Some(rule.gap_id.as_str()),
            field("gap_id"),
            "gap-id for {:?}",
            rule.line_text
        );
    }
    Ok(())
}

