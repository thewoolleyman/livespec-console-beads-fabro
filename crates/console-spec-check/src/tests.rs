#[test]
fn parse_registry_skips_malformed_entries() -> Result<(), String> {
    let json = r#"[
      42,
      {"scenario_file":"scenarios.md","test":"t"},
      {"scenario":"NoFile","test":"t"},
      {"scenario":"OK","scenario_file":"scenarios.md","test":"t",
       "clauses":[5,{"gap_id":"g"},{"gap_id":"g2","scenario":"X"}]},
      {"scenario":"NoTestField","scenario_file":"scenarios.md","clauses":7}
    ]"#;
    let entries = parse_registry(json)?;
    assert_eq!(
        entries.len(),
        2,
        "non-object and field-missing entries are skipped"
    );
    assert_eq!(entries[0].scenario, "OK");
    assert_eq!(
        entries[0].clauses,
        vec![ClauseLink {
            gap_id: "g2".to_string(),
            scenario: "X".to_string()
        }],
    );
    assert_eq!(entries[1].scenario, "NoTestField");
    assert_eq!(entries[1].test, "", "missing test field defaults to empty");
    assert_eq!(
        entries[1].reason, "",
        "missing reason field defaults to empty"
    );
    assert_eq!(
        entries[1].clauses,
        Vec::new(),
        "non-array clauses default to empty"
    );
    Ok(())
}

#[test]
fn evaluate_flags_unlinked_and_resolves_links() {
    // A top-level clause (no heading) keeps the heading_path the predictable
    // "(top)"; an H2 with no preceding H1 would pad a leading empty crumb.
    let spec = "The widget MUST spin.\n";
    let gap = derive_gap_id("spec.md", "(top)", "The widget MUST spin.");
    let sources = [SpecSource {
        spec_file: "spec.md",
        content: spec,
        audience: Audience::Operator,
    }];
    let operator = vec!["Scenario 1 -- A".to_string()];
    let nfr: Vec<String> = Vec::new();

    let report = evaluate(&sources, &[], &operator, &nfr);
    assert_eq!(report.unlinked_clauses.len(), 1, "no registry -> unlinked");
    assert_eq!(report.unlinked_clauses[0].gap_id, gap);

    let linked = vec![CoverageEntry {
        scenario: "Scenario 1 -- A".to_string(),
        scenario_file: "scenarios.md".to_string(),
        test: "t::a".to_string(),
        reason: String::new(),
        clauses: vec![ClauseLink {
            gap_id: gap.clone(),
            scenario: "Scenario 1 -- A".to_string(),
        }],
    }];
    let report = evaluate(&sources, &linked, &operator, &nfr);
    assert!(
        report.unlinked_clauses.is_empty(),
        "linked to a live scenario"
    );
    assert!(
        report.untested_scenarios.is_empty(),
        "scenario now has a test"
    );

    let stale = vec![CoverageEntry {
        scenario: "Ghost".to_string(),
        scenario_file: "scenarios.md".to_string(),
        test: "t".to_string(),
        reason: String::new(),
        clauses: vec![ClauseLink {
            gap_id: gap,
            scenario: "Ghost".to_string(),
        }],
    }];
    assert_eq!(
        evaluate(&sources, &stale, &operator, &nfr)
            .unlinked_clauses
            .len(),
        1,
        "a link to a non-live scenario does not count",
    );
}

#[test]
fn evaluate_partitions_clause_audiences() {
    let nfr_spec = "Contributors MUST lint.\n";
    let gap = derive_gap_id(
        "non-functional-requirements.md",
        "(top)",
        "Contributors MUST lint.",
    );
    let sources = [SpecSource {
        spec_file: "non-functional-requirements.md",
        content: nfr_spec,
        audience: Audience::Contributor,
    }];
    let operator = vec!["Op Scenario".to_string()];
    let nfr = vec!["Nfr Theme".to_string()];

    let wrong = vec![CoverageEntry {
        scenario: "Op Scenario".to_string(),
        scenario_file: "scenarios.md".to_string(),
        test: "t".to_string(),
        reason: String::new(),
        clauses: vec![ClauseLink {
            gap_id: gap.clone(),
            scenario: "Op Scenario".to_string(),
        }],
    }];
    assert_eq!(
        evaluate(&sources, &wrong, &operator, &nfr)
            .unlinked_clauses
            .len(),
        1,
        "a contributor clause linked to an operator scenario is still unlinked",
    );

    let right = vec![CoverageEntry {
        scenario: "Nfr Theme".to_string(),
        scenario_file: NFR_FILE.to_string(),
        test: "t".to_string(),
        reason: String::new(),
        clauses: vec![ClauseLink {
            gap_id: gap,
            scenario: "Nfr Theme".to_string(),
        }],
    }];
    assert!(
        evaluate(&sources, &right, &operator, &nfr)
            .unlinked_clauses
            .is_empty(),
        "linked to the correct-audience NFR scenario",
    );
}

#[test]
fn evaluate_flags_untested_scenarios() {
    let sources: [SpecSource; 0] = [];
    let operator = vec!["Op A".to_string(), "Op B".to_string()];
    let nfr = vec!["Nfr A".to_string()];
    let registry = vec![
        CoverageEntry {
            scenario: "Op A".to_string(),
            scenario_file: "scenarios.md".to_string(),
            test: "t::opa".to_string(),
            reason: String::new(),
            clauses: Vec::new(),
        },
        CoverageEntry {
            scenario: "Op B".to_string(),
            scenario_file: "scenarios.md".to_string(),
            test: "   ".to_string(),
            reason: "ignored for blank tests".to_string(),
            clauses: Vec::new(),
        },
    ];
    let report = evaluate(&sources, &registry, &operator, &nfr);
    let untested: Vec<(&str, &str)> = report
        .untested_scenarios
        .iter()
        .map(|u| (u.scenario_file.as_str(), u.scenario.as_str()))
        .collect();
    assert_eq!(
        untested,
        vec![("scenarios.md", "Op B"), (NFR_FILE, "Nfr A")],
        "whitespace test counts as untested; missing entry counts as untested",
    );
}

