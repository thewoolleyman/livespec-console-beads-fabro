#[test]
fn parse_heading_matches_python_regex() {
    assert_eq!(parse_heading("# Alpha"), Some((1, "Alpha".to_string())));
    assert_eq!(parse_heading("## Beta"), Some((2, "Beta".to_string())));
    assert_eq!(parse_heading("###### Six"), Some((6, "Six".to_string())));
    assert_eq!(
        parse_heading("##   Padded  "),
        Some((2, "Padded".to_string()))
    );
    assert_eq!(
        parse_heading("####### Seven"),
        None,
        "seven hashes is not a heading"
    );
    assert_eq!(
        parse_heading("#nospace"),
        None,
        "no whitespace after hashes"
    );
    assert_eq!(parse_heading("## "), None, "empty title");
    assert_eq!(parse_heading("plain text"), None);
    assert_eq!(parse_heading(""), None);
}

#[test]
fn push_heading_pops_and_pads() {
    let mut stack = vec!["A".to_string(), "B".to_string()];
    push_heading(&mut stack, 1, "C".to_string());
    assert_eq!(stack, vec!["C".to_string()]);
    push_heading(&mut stack, 3, "E".to_string());
    assert_eq!(stack, vec!["C".to_string(), String::new(), "E".to_string()]);
    push_heading(&mut stack, 2, "F".to_string());
    assert_eq!(stack, vec!["C".to_string(), "F".to_string()]);
}

#[test]
fn contains_whole_word_respects_unicode_boundaries() {
    assert!(contains_whole_word("MUST", "MUST"), "whole string");
    assert!(contains_whole_word("a MUST b", "MUST"), "spaces");
    assert!(contains_whole_word("see `MUST` here", "MUST"), "backticks");
    assert!(
        contains_whole_word("a MUST.", "MUST"),
        "trailing punctuation"
    );
    assert!(!contains_whole_word("MUSTARD", "MUST"), "word char after");
    assert!(!contains_whole_word("aMUST", "MUST"), "word char before");
    assert!(!contains_whole_word("MUST_", "MUST"), "underscore after");
    assert!(
        !contains_whole_word("trust", "MUST"),
        "absent (case sensitive)"
    );
    assert!(
        contains_whole_word("aMUST MUST", "MUST"),
        "skip bad occurrence, find good one"
    );
}

